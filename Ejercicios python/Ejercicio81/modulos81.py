"""81.) Crear funciones artiméticas que realicen las operaciones de suma, resta,
multiplicación, división y potencia de enteros"""
def operaciones_modulo(a,b):
    suma=a+b
    resta=a-b
    multiplicacion=a*b
    division=a/b
    potencia=a**b
    return suma,resta,multiplicacion,division,potencia